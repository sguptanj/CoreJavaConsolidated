package com.tcs.ilp_learnings;

public class ModifiedHelloWorld {
	
	public static void main (String [] args)
	{
		printBlock();
		printBlock1();
		printBlock2();
		printBlock3();
		
	}
	
	public static void printBlock()
	{
		System.out.println("Hello World ...");
	}
	public static void printBlock1()
	{
		System.out.println("My name is Siddharth Gupta");
	}
	public static void printBlock2()
	{
		System.out.println("This is my first Java Code");
	}
	public static void printBlock3()
	{
		System.out.println("We are exploring the static keyword");
	}

}


